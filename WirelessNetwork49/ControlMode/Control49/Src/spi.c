/* DMA读取完成                                                          */
/************************************************************************/
void HAL_SPI_TxRxCpltCallback(SPI_HandleTypeDef *hspi)
{
	uint32_t dIndex,addr;
	uint8_t* addr4Save;
	if(hspi->Instance==A7128SPI.Instance)
	{
		A7128_SCS_High();
		if(A7128Work==A7128Work_AbnormalRx)
		{
			AbnormalFrameList[AbnormalFrameIndex].addr=0;
			AbnormalFrameErr=0;
			AbnormalFrameIndex++;
		}
		if(A7128Work==A7128Work_AbnormalRx || A7128Work==A7128Work_RxADCData)
		{
			if(A7128_RxFIFO[pCommandIndex]==ADCDataSendCommand)
			{		
				bNewADCData=1;
				if(A7128_RxFIFO[pSourceIDIndex]<5)
				{
					addr=A7128_RxFIFO[pSourceIDIndex]-1;
					ADCDataTag.H[addr][A7128_RxFIFO[pFrameNumIndex]]=1;
					addr4Save=ADCData4SaveBuf->H[addr][A7128_RxFIFO[pFrameNumIndex]];
				}
				else if(A7128_RxFIFO[pSourceIDIndex]<11)
				{
					addr=A7128_RxFIFO[pSourceIDIndex]-5;
					ADCDataTag.M[addr][A7128_RxFIFO[pFrameNumIndex]]=1;
					addr4Save=ADCData4SaveBuf->M[addr][A7128_RxFIFO[pFrameNumIndex]];
				}
				else
				{
					addr=A7128_RxFIFO[pSourceIDIndex]-11;
					ADCDataTag.L[addr]=1;
					addr4Save=ADCData4SaveBuf->L[addr];
				}
				HAL_DMA_PollForTransfer(&DMA4Copy,HAL_DMA_FULL_TRANSFER,1);
				HAL_DMA_Start(&DMA4Copy,(uint32_t)(&A7128_RxFIFO[pDataIndex]),(uint32_t)(addr4Save),60);
			}
			else if(A7128_RxFIFO[pCommandIndex]==ADCDataSendCommand_BAT)//保存电池电量
			{
				if(A7128_RxFIFO[pSourceIDIndex]<5)
				{
					addr=A7128_RxFIFO[pSourceIDIndex]-1;
					ADCDataTag.H[addr][127]=1;
					addr4Save=ADCData4SaveBuf->H[addr][127];
					SensorBAT.H[addr]=A7128_RxFIFO[pBATIndex];
				}
				else if(A7128_RxFIFO[pSourceIDIndex]<11)
				{
					addr=A7128_RxFIFO[pSourceIDIndex]-5;
					ADCDataTag.M[addr][7]=1;
					addr4Save=ADCData4SaveBuf->M[addr][7];
					SensorBAT.M[addr]=A7128_RxFIFO[pBATIndex];
				}
				else
				{
					addr=A7128_RxFIFO[pSourceIDIndex]-11;
					ADCDataTag.L[addr]=1;
					addr4Save=ADCData4SaveBuf->L[addr];
					SensorBAT.L[addr]=A7128_RxFIFO[pBATIndex];
				}
				HAL_DMA_PollForTransfer(&DMA4Copy,HAL_DMA_FULL_TRANSFER,1);
				HAL_DMA_Start(&DMA4Copy,(uint32_t)(&A7128_RxFIFO[pDataIndex]),(uint32_t)(addr4Save),60);
			}
			if(A7128Work==A7128Work_RxADCData)
			{
				A7128_SetRx();
			}
		}		
	}
}
